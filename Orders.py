	def proceedcancel(self):
		issue=self.issuecomboBox.currentText()
		#print(str(issue))
		desc= self.canceldesctextEdit.toPlainText()
		R=self.RRcomboBox.currentText()
		#print(str(R))

		ordno=self.orderidline.text()
		shid="1" #value from shipping.py

		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			cur=conn.cursor()
			
			cur.execute("update ShippingDetails set tracking='Cancelled' where ord_no ="+str(ordno))
			cur.execute("update OrderStatus set status='Cancelled' where ord_no ="+str(ordno))
			cur.execute("update OrderStatus set reason_for_return = '"+str(issue)+"' where ord_no ="+str(ordno))
			cur.execute("update OrderStatus set cancel_desc = '"+str(desc)+"' where ord_no = "+str(ordno))
			cur.execute("update OrderStatus set ret_or_rep = '"+str(R)+"' where ord_no = "+str(ordno))

		if R=="REPLACE":
			self.replacetext.show()
			self.refundtext.hide()

		if R=="RETURN & REFUND":
			self.refundtext.show()
			self.replacetext.hide()

		self.cancelproceedButton.setEnabled(False)

