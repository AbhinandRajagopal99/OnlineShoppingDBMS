	def LoadInfo(self):
		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			cur=conn.cursor()
			cur.execute("select d.order_date, o.ord_no,status from Cart d,OrderStatus o,Customers c1 where c1.cus_id=d.cus_id and d.ord_no = o.ord_no and login=1")
			result=cur.fetchall()

		self.od_details.setRowCount(0)
		for row_number,row_data in enumerate(result):
			self.od_details.insertRow(row_number)
			for column_number,data in enumerate(row_data):
				cell=QtWidgets.QTableWidgetItem(str(data))
				cell.setFlags(QtCore.Qt.ItemIsEnabled)
				self.od_details.setItem(row_number,column_number,cell)


	def Loadshipp(self):
		itemindex = self.od_details.currentRow()
		item = self.od_details.item(itemindex,1).text()
		self.orderidline.setText(str(item))
		self.OrdergroupBox.hide()
		self.ShipgroupBox.show()
		self.LoadShipment()

	def Loadfeed(self):
		itemindex = self.od_details.currentRow()
		item = self.od_details.item(itemindex,1).text()
		self.cusfeedpdtid.setText(str(item))
		self.OrdergroupBox.hide()
		self.FeebackgroupBox.show()
		self.whenOpen()

	def LoadData(self):
		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			odno=self.orderidline.text()
			cur=conn.cursor()
			cur.execute("select tracking, date, ship_add from ShippingDetails s where s.ord_no ="+str(odno))
			results=cur.fetchall()

			for rows in results:
				self.statusline.setText(str(rows[0]))
				self.delivarytimelabel.setText(str(rows[1]))
				self.presentlocationlabel.setText(str(rows[2]))

			stat=self.statusline.text()

			if str(stat)=="Cancelled":
				self.Cancelorderbutton.hide()
			else:
				self.Cancelorderbutton.show()

	def whenOpen(self):
		ordno=self.cusfeedpdtid.text()
		self.textEditgetdesc.setText("")
		self.cusfeedpdtname.setText("")
		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			cur=conn.cursor()
			cur.execute("select pdt_id from OrderDetails where ord_no = "+str(ordno))
			result=cur.fetchall()

			for row in result:
				id=row[0]
				self.pdtidcomboBox.addItem(str(id))

	def pdtok(self):

		id=self.pdtidcomboBox.currentText()
		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			cur=conn.cursor()
			cur.execute("select product_name from ProductDetails where pdt_id = "+str(id))
			result=cur.fetchone()[0]
			self.cusfeedpdtname.setText(str(result))

	def enterfeedback(self):
		desc=self.textEditgetdesc.toPlainText()
		rate=self.Ratingslider.value()
		rate=rate+1
		orno=self.cusfeedpdtid.text()
		pdid=self.pdtidcomboBox.currentText()

		conn=mdb.connect("localhost","onshop","mysql","onlineshop")
		with conn:
			cur=conn.cursor()
			cur.execute("select cus_id from Customers where login =1")
			cus=cur.fetchone()[0]

			cur.execute("insert into Feedbacks values("+str(cus)+","+str(pdid)+","+str(rate)+",'"+str(desc)+"')")

